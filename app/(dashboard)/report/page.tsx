const loadJsPDF = () => {
  return new Promise((resolve) => {
    if ((window as any).jspdf) return resolve((window as any).jspdf);
    const script = document.createElement("script");
    script.src = "https://cdnjs.cloudflare.com/ajax/libs/jspdf/2.5.1/jspdf.umd.min.js";
    script.onload = () => resolve((window as any).jspdf);
    document.head.appendChild(script);
  });
};

const buildPdfDocument = async (formData: any, metrics: any, kpis: any) => {
  const jspdfModule: any = await loadJsPDF();
  const { jsPDF } = jspdfModule;
  const doc = new jsPDF();
  const themeColor = formData.theme === "Professional" ? [79, 70, 229] : [16, 185, 129];

  // PAGE 1: COVER
  doc.setFillColor(themeColor[0], themeColor[1], themeColor[2]);
  doc.rect(0, 0, 210, 297, "F");
  doc.setTextColor(255, 255, 255);
  doc.setFontSize(50); doc.text("FORS", 105, 90, { align: "center" });
  doc.setFontSize(14); doc.text("SYSTEM REPORT", 105, 100, { align: "center" });
  doc.setDrawColor(255, 255, 255); doc.line(80, 105, 130, 105);
  doc.setFontSize(28); doc.text(formData.name.toUpperCase(), 105, 160, { align: "center", maxWidth: 160 });
  doc.setFontSize(10); doc.text(`GENERATED BY SYSTEM ON ${new Date().toLocaleDateString()}`, 105, 260, { align: "center" });

  // PAGE 2: DASHBOARD
  doc.addPage();
  doc.setFillColor(themeColor[0], themeColor[1], themeColor[2]);
  doc.rect(0, 0, 210, 25, "F");
  doc.setTextColor(255, 255, 255); doc.setFontSize(12); doc.text("EXECUTIVE PERFORMANCE DASHBOARD", 20, 16);

  let y = 45;
  doc.setTextColor(30, 41, 59); doc.setFontSize(18); doc.text("OPERATIONAL TELEMETRY", 20, y);
  y += 10;
  doc.setTextColor(100, 116, 139); doc.setFontSize(9); doc.text(doc.splitTextToSize(formData.description || "", 170), 20, y);
  y += 20;

  const activeMetrics: { label: string; val: string; sub: string; }[] = [];
  if (metrics.reliability) activeMetrics.push({ label: "RELIABILITY INDEX", val: `${kpis.totalTickets > 0 ? Math.round((kpis.closedTickets / kpis.totalTickets) * 100) : 0}%`, sub: "Resolution Velocity" });
  if (metrics.aiAccuracy) activeMetrics.push({ label: "AI CONFIDENCE", val: `${kpis.avgAiConfidence}%`, sub: "Model Accuracy" });
  if (metrics.efficiency) activeMetrics.push({ label: "AUTOMATION RATE", val: `${kpis.sqlProposedTickets}`, sub: "SQL Generation" });
  if (metrics.incidentVolume) activeMetrics.push({ label: "INCIDENT VOLUME", val: `${kpis.totalTickets}`, sub: "Total Tickets" });

  activeMetrics.forEach((m, idx) => {
    doc.setFillColor(248, 250, 252); doc.roundedRect(20, y, 170, 22, 3, 3, "F");
    doc.setTextColor(100, 116, 139); doc.setFontSize(7); doc.text(m.label, 28, y + 8);
    doc.setTextColor(themeColor[0], themeColor[1], themeColor[2]); doc.setFontSize(13); doc.text(m.val, 28, y + 16);
    doc.setTextColor(148, 163, 184); doc.setFontSize(7); doc.text(m.sub, 100, y + 13);
    y += 26;
  });

  if (formData.notes) {
    doc.addPage();
    doc.setFillColor(themeColor[0], themeColor[1], themeColor[2]);
    doc.rect(0, 0, 210, 25, "F");
    doc.setTextColor(255, 255, 255); doc.setFontSize(12); doc.text("EXECUTIVE NARRATIVES", 20, 16);
    y = 45;
    doc.setTextColor(30, 41, 59); doc.setFontSize(18); doc.text("MANAGEMENT OBSERVATIONS", 20, y);
    y += 15;
    doc.setTextColor(71, 85, 105); doc.setFontSize(10);
    doc.text(doc.splitTextToSize(formData.notes, 170), 20, y);
  }

  return doc;
};

