function PdfConfigModal({ onClose, kpis, onPreviewReady }: any) {
  const [step, setStep] = useState<"form" | "generating" | "success">("form");
  const [formData, setFormData] = useState({
    name: `Report_${new Date().toLocaleDateString().replace(/\//g, '-')}`,
    description: "Operational Synthesis & Strategic IT Report.",
    theme: "Professional",
    template: "exec",
    notes: "",
  });
  const [metrics, setMetrics] = useState({
    reliability: true,
    efficiency: true,
    aiAccuracy: true,
    workload: true,
    slaCompliance: true,
    incidentVolume: true,
    teamDistribution: false
  });

  const handleTemplateChange = (tid: string) => {
    const t = TEMPLATES.find(x => x.id === tid);
    if (!t) return;
    setFormData(prev => ({ ...prev, template: tid, name: `FORS_${t.name.replace(/\s+/g, '_')}_${new Date().toLocaleDateString().replace(/\//g, '-')}` }));
    if (tid === "exec") setMetrics({ reliability: true, efficiency: true, aiAccuracy: true, workload: false, slaCompliance: true, incidentVolume: false, teamDistribution: false });
    if (tid === "ops") setMetrics({ reliability: true, efficiency: true, aiAccuracy: true, workload: true, slaCompliance: true, incidentVolume: true, teamDistribution: true });
    if (tid === "tech") setMetrics({ reliability: true, efficiency: false, aiAccuracy: true, workload: true, slaCompliance: false, incidentVolume: true, teamDistribution: false });
  };

  const handleGenerate = async () => {
    setStep("generating");
    await logReportGenerationAction({ name: formData.name, description: formData.description, theme: formData.theme, config: { metrics, notes: formData.notes } });
    try {
      const doc = await buildPdfDocument(formData, metrics, kpis);
      setTimeout(() => {
        onPreviewReady(doc, formData.name);
      }, 1200);
    } catch (e) { setStep("success"); }
  };

  return (
    <div className="fixed inset-0 z-20 flex items-center justify-center p-6">
      <div className="fixed inset-0 bg-slate-900/60 backdrop-blur-md animate-in fade-in duration-300" onClick={onClose} />

      <div className="relative bg-white w-full max-w-xl rounded-[3rem] shadow-[0_50px_100px_-20px_rgba(0,0,0,0.6)] overflow-hidden animate-in zoom-in-95 duration-200 flex flex-col border border-white/20">
        {step === "form" && (
          <div className="flex flex-col h-full max-h-[85vh]">
            <div className="p-8 border-b border-white/10 bg-gradient-to-br from-slate-900 via-indigo-950 to-indigo-900 relative shrink-0">
              <div className="absolute top-0 right-0 w-48 h-48 bg-indigo-500/20 rounded-full blur-3xl -mr-24 -mt-24 animate-pulse" />
              <div className="relative z-10 flex justify-between items-center">
                <div>
                  <h3 className="text-2xl font-black text-white tracking-tight leading-none">Report Generator</h3>
                  <p className="text-[10px] font-bold text-indigo-400 uppercase tracking-[0.2em] mt-3">Report Configuration</p>
                </div>
                <button onClick={onClose} className="w-10 h-10 bg-white/5 hover:bg-white/10 border border-white/10 flex items-center justify-center rounded-xl transition-all text-white/50 hover:text-white"><X className="w-5 h-5" /></button>
              </div>
            </div>

            <div className="flex-1 p-8 space-y-8 overflow-y-auto scrollbar-hide bg-slate-50/50">
              {/* Template Selector */}
              <div className="space-y-3">
                <label className="text-[11px] font-black text-slate-400 uppercase tracking-widest flex items-center gap-2"><Trophy className="w-4 h-4 text-amber-500" /> Report Template</label>
                <div className="grid grid-cols-2 gap-3">
                  {TEMPLATES.map(t => (
                    <button key={t.id} onClick={() => handleTemplateChange(t.id)} className={clsx("p-4 rounded-2xl border-2 transition-all text-left group", formData.template === t.id ? "bg-white border-indigo-500 shadow-xl" : "bg-white/60 border-transparent hover:border-slate-200")}>
                      <p className={clsx("text-xs font-black uppercase tracking-widest", formData.template === t.id ? "text-indigo-600" : "text-slate-700")}>{t.name}</p>
                      <p className="text-[9px] font-medium text-slate-400 mt-1 line-clamp-1">{t.desc}</p>
                    </button>
                  ))}
                </div>
              </div>

              <div className="grid grid-cols-2 gap-4">
                <div className="space-y-2">
                  <label className="text-[11px] font-black text-slate-400 uppercase tracking-widest flex items-center gap-2"><Type className="w-4 h-4 text-indigo-500" /> Report Name</label>
                  <input type="text" className="w-full px-5 py-4 bg-white border border-slate-200/60 rounded-2xl text-xs font-bold outline-none focus:ring-4 focus:ring-indigo-100 transition-all shadow-sm" value={formData.name} onChange={e => setFormData(prev => ({ ...prev, name: e.target.value }))} />
                </div>
                <div className="space-y-2">
                  <label className="text-[11px] font-black text-slate-400 uppercase tracking-widest flex items-center gap-2"><Palette className="w-4 h-4 text-rose-500" /> Theme</label>
                  <div className="flex gap-2 p-1.5 bg-white border border-slate-200/60 rounded-2xl shadow-sm">
                    {["Professional", "Analytical"].map(t => (
                      <button key={t} onClick={() => setFormData(prev => ({ ...prev, theme: t }))} className={clsx("flex-1 py-2.5 rounded-xl text-[9px] font-black uppercase tracking-widest transition-all", formData.theme === t ? "bg-slate-900 text-white shadow-lg" : "text-slate-400 hover:bg-slate-50")}>{t}</button>
                    ))}
                  </div>
                </div>
              </div>

              <div className="space-y-3">
                <label className="text-[11px] font-black text-slate-400 uppercase tracking-widest flex items-center gap-2"><BarChart3 className="w-4 h-4 text-violet-500" /> Metrics to Include</label>
                <div className="grid grid-cols-2 gap-3">
                  {Object.entries(metrics).map(([key, val]) => (
                    <button key={key} onClick={() => setMetrics(prev => ({ ...prev, [key]: !val }))} className={clsx("p-4 rounded-2xl border transition-all flex items-center justify-between group", val ? "bg-white border-indigo-200 text-indigo-600 shadow-lg ring-2 ring-indigo-50" : "bg-white/40 border-slate-100 text-slate-400")}>
                      <div className="flex items-center gap-3">
                        <div className={clsx("w-6 h-6 rounded-lg flex items-center justify-center transition-all", val ? "bg-indigo-500 text-white shadow-lg" : "bg-slate-100 text-slate-300")}>
                          {val ? <Check className="w-3.5 h-3.5" /> : <ChevronDown className="w-3 h-3" />}
                        </div>
                        <p className="text-[9px] font-black uppercase tracking-widest">{key.replace(/([A-Z])/g, ' $1')}</p>
                      </div>
                    </button>
                  ))}
                </div>
              </div>

              <div className="space-y-3">
                <label className="text-[11px] font-black text-slate-400 uppercase tracking-widest flex items-center gap-2"><PenLine className="w-4 h-4 text-amber-500" /> Custom Notes</label>
                <textarea className="w-full px-5 py-4 bg-white border border-slate-200/60 rounded-2xl text-xs font-bold h-32 resize-none outline-none focus:ring-4 focus:ring-indigo-100 transition-all shadow-sm" value={formData.notes} onChange={e => setFormData(prev => ({ ...prev, notes: e.target.value }))} placeholder="Add custom notes or comments..." />
              </div>
            </div>

            <div className="p-8 bg-white border-t border-slate-100 shrink-0">
              <button onClick={handleGenerate} className="w-full py-5 bg-slate-900 text-white rounded-[2.5rem] font-black uppercase tracking-widest text-xs shadow-[0_30px_60px_-15px_rgba(0,0,0,0.4)] hover:bg-black hover:-translate-y-1 transition-all flex items-center justify-center gap-4 border-b-4 border-slate-700">
                <FileDown className="w-6 h-6 text-indigo-400" /> Generate Report
              </button>
            </div>
          </div>
        )}
        {step === "generating" && (<div className="p-24 flex flex-col items-center text-center space-y-6"><Loader2 className="w-16 h-16 text-indigo-600 animate-spin" /><h3 className="text-xl font-black text-slate-800 tracking-tight">Generating Report...</h3><p className="text-[10px] font-bold text-slate-400 uppercase tracking-widest">Integrating selected metrics</p></div>)}
        {step === "success" && (<div className="p-24 flex flex-col items-center text-center space-y-8 animate-in zoom-in-95"><FileCheck className="w-24 h-24 text-emerald-500" /><h3 className="text-2xl font-black text-slate-800 tracking-tight">Report Generated</h3><button onClick={onClose} className="w-full py-5 bg-emerald-500 text-white rounded-[2rem] font-black uppercase tracking-widest text-[10px] shadow-xl">Back to Overview</button></div>)}
      </div>
    </div>
  );
}
