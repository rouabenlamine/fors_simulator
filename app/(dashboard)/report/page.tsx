function SmallInsightCard({ icon: Icon, label, value, trend, sub, color, delay }: any) {
  const themes: any = {
    indigo: "from-indigo-500 to-indigo-600 text-indigo-600 bg-indigo-50 border-indigo-100",
    blue: "from-blue-500 to-blue-600 text-blue-600 bg-blue-50 border-blue-100",
    amber: "from-amber-500 to-amber-600 text-amber-600 bg-amber-50 border-amber-100",
    fuchsia: "from-fuchsia-500 to-fuchsia-600 text-fuchsia-600 bg-fuchsia-50 border-fuchsia-100",
    rose: "from-rose-500 to-rose-600 text-rose-600 bg-rose-50 border-rose-100",
    cyan: "from-cyan-500 to-cyan-600 text-cyan-600 bg-cyan-50 border-cyan-100",
    emerald: "from-emerald-500 to-emerald-600 text-emerald-600 bg-emerald-50 border-emerald-100",
  };
  const theme = themes[color] || themes.indigo;
  const colors = theme.split(' ');

  return (
    <div style={{ animationDelay: `${delay}ms` }} className="bg-white rounded-2xl border border-slate-100 p-4 shadow-sm hover:shadow-lg hover:-translate-y-1 transition-all duration-300 group flex flex-col h-full min-h-[120px] animate-in fade-in slide-in-from-bottom-2 fill-mode-both">
      <div className="flex items-center justify-between mb-3">
        <div className={clsx("w-9 h-9 rounded-xl flex items-center justify-center shrink-0 shadow-sm bg-gradient-to-br", colors[0], colors[1])}>
          <Icon className="w-4.5 h-4.5 text-white" />
        </div>
        <div className={clsx("px-1.5 py-0.5 rounded-lg text-[8px] font-black uppercase tracking-widest", colors[3], colors[4])}>
          {trend}
        </div>
      </div>
      <div className="space-y-0.5 mt-auto">
        <p className="text-[8px] font-black text-slate-400 uppercase tracking-widest leading-none">{label}</p>
        <p className={clsx("text-lg font-black tracking-tight leading-none bg-clip-text text-transparent bg-gradient-to-r", colors[0], colors[1])}>{value}</p>
        <p className="text-[9px] font-medium text-slate-400 leading-tight italic truncate">"{sub}"</p>
      </div>
    </div>
  );
}

